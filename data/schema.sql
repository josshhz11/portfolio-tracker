/*
    Table Schema Setup - for Holdings, Daily_Prices, Currencies, Trades,
    Cash_Accounts, Cash_Ledger, Cash_Snapshots
*/

create table if not exists public.holdings (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    ticker text not null,
    shares_owned numeric(20,6) not null default 0 check (shares_owned >= 0),
    invested_amount numeric(20,6) not null default 0 check (invested_amount >= 0),
    currency text not null,
    platform text not null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique (user_id, ticker, platform, currency)
);

create table if not exists public.daily_prices (
    id bigint generated by default as identity primary key,
    ticker text not null,
    price_per_share numeric(20,6) not null check (price_per_share >= 0),
    price_date date not null,
    created_at timestamptz not null default now(),
    unique (ticker, price_date)
);

create table if not exists public.currencies (
    id bigint generated by default as identity primary key,
    currency text not null,
    rate numeric(20,6) not null check (rate > 0),
    rate_date date not null,
    created_at timestamptz not null default now(),
    unique (currency, rate_date)
);

create table if not exists public.trades (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    holding_id bigint references public.holdings(id) on delete cascade,
    ticker text not null,
    trade_type text not null check (trade_type in ('BUY', 'SELL')),
    currency text not null,
    cash_amount numeric(20,6) not null check (cash_amount > 0),
    shares numeric(20,6) not null check (shares > 0),
    platform text not null,
    traded_at timestamptz not null,
    created_at timestamptz not null default now()
);

create table if not exists public.portfolio_snapshots (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    holding_id bigint not null references public.holdings(id) on delete cascade,
    snapshot_date date not null,
    shares_owned numeric(20,6) not null,
    invested_amount numeric(20,6) not null,
    price_per_share numeric(20,6) not null,
    fx_rate numeric(20,6) not null,
    market_value numeric(20,6) not null,
    market_value_sgd numeric(20,6) not null,
    unrealized_profit numeric(20,6) not null,
    unrealized_profit_sgd numeric(20,6) not null,
    created_at timestamptz not null default now(),
    unique (holding_id, snapshot_date)
);

create table if not exists public.cash_accounts (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    platform text not null,
    currency text not null,
    balance numeric(20,6) not null default 0 check (balance >= 0),
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique (user_id, platform, currency)
);

create table if not exists public.cash_snapshots (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    cash_account_id bigint not null references public.cash_accounts(id) on delete cascade,
    snapshot_date date not null,
    platform text not null,
    currency text not null,
    balance numeric(20,6) not null,
    fx_rate numeric(20,6) not null,
    balance_sgd numeric(20,6) not null,
    created_at timestamptz not null default now(),
    unique (cash_account_id, snapshot_date)
);

create table if not exists public.cash_ledger (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    platform text not null,
    entry_type text not null check (entry_type in ('DEPOSIT', 'WITHDRAWAL', 'TRADE_BUY', 'TRADE_SELL', 'FX_EXCHANGE')),
    currency text not null,
    amount numeric(20,6) not null check (amount > 0),
    counter_currency text,
    counter_amount numeric(20,6),
    fx_rate numeric(20,6),
    trade_id bigint references public.trades(id) on delete set null,
    note text,
    created_at timestamptz not null default now(),
    constraint chk_cash_ledger_fx_fields
        check (
            (
                entry_type in ('DEPOSIT', 'WITHDRAWAL')
                and counter_currency is null
                and counter_amount is null
                and fx_rate is null
                and trade_id is null
            )
            or
            (
                entry_type in ('TRADE_BUY', 'TRADE_SELL')
                and trade_id is not null
                and counter_currency is null
                and counter_amount is null
                and fx_rate is null
            )
            or
            (
                entry_type = 'FX_EXCHANGE'
                and counter_currency is not null
                and counter_currency <> currency
                and counter_amount is not null
                and fx_rate is not null
                and trade_id is null
            )
        )
);

