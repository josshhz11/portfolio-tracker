/*
    BEFORE INSERT trigger:
    - resolve holding_id if missing
    - auto-create holding on BUY if it does not exist
    - reject SELL if holding does not exist
*/
create or replace function public.resolve_trade_holding()
returns trigger
language plpgsql
as $$
declare
    v_holding_id bigint;
    v_holding_user_id uuid;
begin
    -- If holding_id is already provided, validate it belongs to the same user
    if new.holding_id is not null then
        select h.user_id
        into v_holding_user_id
        from public.holdings h
        where h.id = new.holding_id;

        if v_holding_user_id is null then
            raise exception 'Holding % does not exist', new.holding_id;
        end if;

        if v_holding_user_id <> new.user_id then
            raise exception 'Holding % does not belong to user %', new.holding_id, new.user_id;
        end if;

        return new;
    end if;

    -- Try to find an existing holding for this user using ticker/platform/currency
    select h.id
    into v_holding_id
    from public.holdings h
    where h.user_id = new.user_id
      and h.ticker = new.ticker
      and h.platform = new.platform
      and h.currency = new.currency
    limit 1;

    if v_holding_id is not null then
        new.holding_id := v_holding_id;
        return new;
    end if;

    -- If not found and this is a BUY, create the holding automatically
    if new.trade_type = 'BUY' then
        insert into public.holdings (
            user_id,
            ticker,
            shares_owned,
            invested_amount,
            currency,
            platform
        )
        values (
            new.user_id,
            new.ticker,
            0,
            0,
            new.currency,
            new.platform
        )
        returning id into v_holding_id;

        new.holding_id := v_holding_id;
        return new;
    end if;

    -- If not found and this is a SELL, reject it
    raise exception
        'Cannot SELL: no holding exists for user %, ticker %, platform %, currency %',
        new.user_id, new.ticker, new.platform, new.currency;
end;
$$;

drop trigger if exists trg_trades_resolve_holding on public.trades;

create trigger trg_trades_resolve_holding
before insert on public.trades
for each row
execute function public.resolve_trade_holding();


/*
    AFTER INSERT trigger:
    - BUY: add shares and cash_amount to holdings
    - SELL: validate shares, reduce shares, reduce invested_amount by average cost basis
*/
create or replace function public.apply_trade_to_holding()
returns trigger
language plpgsql
as $$
declare
    v_current_shares numeric(20,6);
    v_current_invested numeric(20,6);
    v_holding_user_id uuid;
    v_avg_cost numeric(20,6);
    v_cost_reduction numeric(20,6);
begin
    select h.user_id, h.shares_owned, h.invested_amount
    into v_holding_user_id, v_current_shares, v_current_invested
    from public.holdings h
    where h.id = new.holding_id
    for update;

    if not found then
        raise exception 'Holding % not found for trade %', new.holding_id, new.id;
    end if;

    if v_holding_user_id <> new.user_id then
        raise exception 'Trade user_id does not match holding owner';
    end if;

    if new.trade_type = 'BUY' then
        update public.holdings
        set shares_owned = shares_owned + new.shares,
            invested_amount = invested_amount + new.cash_amount
        where id = new.holding_id;

        return new;
    end if;

    -- SELL logic
    if new.shares > v_current_shares then
        raise exception
            'Cannot SELL %.6f shares: holding only has %.6f shares',
            new.shares, v_current_shares;
    end if;

    if v_current_shares = 0 then
        raise exception 'Cannot SELL from holding % with zero shares', new.holding_id;
    end if;

    v_avg_cost := v_current_invested / v_current_shares;
    v_cost_reduction := new.shares * v_avg_cost;

    update public.holdings
    set shares_owned = shares_owned - new.shares,
        invested_amount = greatest(invested_amount - v_cost_reduction, 0)
    where id = new.holding_id;

    return new;
end;
$$;

drop trigger if exists trg_trades_apply_to_holding on public.trades;

create trigger trg_trades_apply_to_holding
after insert on public.trades
for each row
execute function public.apply_trade_to_holding();

/*
