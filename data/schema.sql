/*
    Table Schema Setup - for Holdings, Daily_Prices, Currencies, Trades 
*/

create table if not exists public.holdings (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    ticker text not null,
    shares_owned numeric(20,6) not null default 0 check (shares_owned >= 0),
    invested_amount numeric(20,6) not null default 0 check (invested_amount >= 0),
    currency text not null,
    platform text not null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique (user_id, ticker, platform, currency)
);

create table if not exists public.daily_prices (
    id bigint generated by default as identity primary key,
    ticker text not null,
    price_per_share numeric(20,6) not null check (price_per_share >= 0),
    price_date date not null,
    created_at timestamptz not null default now(),
    unique (ticker, price_date)
);

create table if not exists public.currencies (
    id bigint generated by default as identity primary key,
    currency text not null,
    rate numeric(20,6) not null check (rate > 0),
    rate_date date not null,
    created_at timestamptz not null default now(),
    unique (currency, rate_date)
);

create table if not exists public.trades (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    holding_id bigint references public.holdings(id) on delete cascade,
    ticker text not null,
    trade_type text not null check (trade_type in ('BUY', 'SELL')),
    currency text not null,
    cash_amount numeric(20,6) not null check (cash_amount > 0),
    shares numeric(20,6) not null check (shares > 0),
    platform text not null,
    traded_at timestamptz not null,
    created_at timestamptz not null default now()
);

