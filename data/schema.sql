/*
    BEFORE INSERT trigger on cash_ledger:
    - ensure the source cash account exists for every ledger row
    - ensure the destination cash account exists for FX exchange rows
*/
create or replace function public.ensure_cash_accounts_for_ledger()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
begin
    insert into public.cash_accounts (user_id, platform, currency)
    values (new.user_id, new.platform, new.currency)
    on conflict (user_id, platform, currency) do nothing;

    if new.entry_type = 'FX_EXCHANGE' then
        insert into public.cash_accounts (user_id, platform, currency)
        values (new.user_id, new.platform, new.counter_currency)
        on conflict (user_id, platform, currency) do nothing;
    end if;

    return new;
end;
$$;

drop trigger if exists trg_cash_ledger_ensure_accounts on public.cash_ledger;

create trigger trg_cash_ledger_ensure_accounts
before insert on public.cash_ledger
for each row
execute function public.ensure_cash_accounts_for_ledger();

/*
    AFTER INSERT trigger on cash_ledger:
    - apply deposits and sell proceeds to the source cash account
    - apply withdrawals and buy cash movements as debits
    - apply FX exchange as a debit on one cash account and a credit on the counter account
*/
create or replace function public.apply_cash_ledger_to_accounts()
returns trigger
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
    v_updated_balance numeric(20,6);
begin
    if new.entry_type in ('DEPOSIT', 'TRADE_SELL') then
        update public.cash_accounts
        set balance = balance + new.amount
        where user_id = new.user_id
          and platform = new.platform
          and currency = new.currency
        returning balance into v_updated_balance;

        if not found then
            raise exception
                'Cash account not found for user %, platform %, currency %',
                new.user_id, new.platform, new.currency;
        end if;

        return new;
    end if;

    if new.entry_type in ('WITHDRAWAL', 'TRADE_BUY') then
        update public.cash_accounts
        set balance = balance - new.amount
        where user_id = new.user_id
          and platform = new.platform
          and currency = new.currency
          and balance >= new.amount
        returning balance into v_updated_balance;

        if not found then
            raise exception
                'Insufficient cash balance for user %, platform %, currency %: required %',
                new.user_id, new.platform, new.currency, new.amount;
        end if;

        return new;
    end if;

    if new.entry_type = 'FX_EXCHANGE' then
        update public.cash_accounts
        set balance = balance - new.amount
        where user_id = new.user_id
          and platform = new.platform
          and currency = new.currency
          and balance >= new.amount
        returning balance into v_updated_balance;

        if not found then
            raise exception
                'Insufficient cash balance for FX exchange on user %, platform %, currency %: required %',
                new.user_id, new.platform, new.currency, new.amount;
        end if;

        update public.cash_accounts
        set balance = balance + new.counter_amount
        where user_id = new.user_id
          and platform = new.platform
          and currency = new.counter_currency
        returning balance into v_updated_balance;

        if not found then
            raise exception
                'Target cash account not found for FX exchange on user %, platform %, currency %',
                new.user_id, new.platform, new.counter_currency;
        end if;

        return new;
    end if;

    raise exception 'Unsupported cash ledger entry type: %', new.entry_type;
end;
$$;

drop trigger if exists trg_cash_ledger_apply_to_accounts on public.cash_ledger;

create trigger trg_cash_ledger_apply_to_accounts
after insert on public.cash_ledger
for each row
execute function public.apply_cash_ledger_to_accounts();

